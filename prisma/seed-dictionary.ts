async function main() {
  console.log('📖 Seeding dictionary entries...')

  let created = 0
  let updated = 0

  for (const entry of ENTRIES) {
    const wordLower = entry.word.toLowerCase()
    const existing = await db.dictionaryEntry.findUnique({
      where: { wordLower },
      select: { id: true },
    })

    await db.dictionaryEntry.upsert({
      where: { wordLower },
      update: {
        word: entry.word,
        persian: entry.persian,
        pos: entry.pos || '',
        phonetic: entry.phonetic || '',
      },
      create: {
        word: entry.word,
        wordLower,
        persian: entry.persian,
        pos: entry.pos || '',
        phonetic: entry.phonetic || '',
      },
    })

    if (existing) updated++
    else created++
  }

  console.log(`✅ Done. Created ${created}, updated ${updated}.`)
  console.log(`   Total entries: ${await db.dictionaryEntry.count()}`)
}

main()
  .catch((e) => {
    console.error('❌ Seed failed:', e)
    process.exit(1)
  })
  .finally(async () => {
    await db.$disconnect()
  })
