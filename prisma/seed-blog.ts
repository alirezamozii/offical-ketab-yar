async function main() {
  console.log('🌱 Seeding blog posts...')

  // Ensure there's at least one admin/owner user to be the author.
  let author = await db.user.findFirst({
    where: { role: { in: ['OWNER', 'ADMIN'] } },
    select: { id: true, name: true, email: true, role: true },
  })

  if (!author) {
    console.log('  No OWNER/ADMIN found — creating system editorial user...')
    author = await db.user.create({
      data: {
        email: 'editorial@ketabyar.ir',
        name: 'هیئت تحریریه کتاب‌یار',
        role: 'ADMIN',
        // No password — this is a system account, login disabled.
      },
      select: { id: true, name: true, email: true, role: true },
    })
    console.log(`  ✓ Created editorial user: ${author.email} (${author.id})`)
  } else {
    console.log(`  Using existing author: ${author.email} (${author.role})`)
  }

  let created = 0
  let updated = 0
  const skipped = 0 // reserved for future "skip-if-unchanged" logic

  for (const post of POSTS) {
    const slug = post.slug
    const existing = await db.blogPost.findUnique({
      where: { slug },
      select: { id: true, updatedAt: true, publishedAt: true },
    })

    const result = await db.blogPost.upsert({
      where: { slug },
      update: {
        title: post.title,
        excerpt: post.excerpt,
        content: post.content,
        tags: JSON.stringify(post.tags),
        published: true,
        publishedAt: existing?.publishedAt ?? new Date(),
        authorId: author.id,
      },
      create: {
        slug,
        title: post.title,
        excerpt: post.excerpt,
        content: post.content,
        tags: JSON.stringify(post.tags),
        coverUrl: '', // procedural cover rendered client-side from coverFrom/coverTo
        published: true,
        publishedAt: new Date(),
        authorId: author.id,
      },
      select: { id: true, slug: true, title: true },
    })

    if (existing) {
      // Check if content actually changed (avoid bumping updatedAt needlessly)
      updated++
      console.log(`  • Updated: ${result.title}`)
    } else {
      created++
      console.log(`  ✓ Created: ${result.title}`)
    }
  }

  console.log('')
  console.log(`✅ Done. Created ${created}, updated ${updated}, skipped ${skipped}.`)
  console.log(`   Total published posts: ${await db.blogPost.count({ where: { published: true } })}`)
}

